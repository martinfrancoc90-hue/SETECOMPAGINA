                        <p class="text-justify">
                            Un sistema de administración de edificio (BMS) es un programa operado por
                            computadora para centralizar y optimizar el monitoreo, operación y
                            administración de un edificio. Por esta razón, un BMS es considerado el sistema
                            nervioso central de un edificio. El concepto de “edificio inteligente” bajo el cual
                            está basado el BMS utiliza lo último en tecnología para decidir de manera
                            inteligente cómo deben operar de manera óptima los sistemas eléctricos y
                            mecánicos de un edificio.
                        </p>
                        <p class="text-justify">
                            La función central de un BMS es la de administrar efectivamente un edificio al
                            balancear el confort y la eficiencia. Dependiendo del tamaño del edificio y del
                            hardware y software instalados, el BMS puede monitorear y controlar la
                            temperatura, humedad, distribución del aire, niveles de dióxido de carbono,
                            iluminación y cortinas. El sistema también puede estar ligado a las funciones de
                            controles de acceso al edificio, controles de seguridad y procedimientos, alarmas
                            de fuego, elevadores, electricidad y a los espacios de estacionamiento
                            conectados.
                        </p>
                        <p class="text-justify">
                            Un BMS asegura que los espacios interiores para un usuario mantendrán una
                            temperatura agradable y constante. Algunos diseños facilitan el control individual
                            por habitación. Esta característica puede ser útil en áreas como salas de juntas, en
                            donde se reúnen grandes cantidades de personas, por lo cual el espacio necesita
                            ventilación adicional.
